 * –º–µ—Ç–æ–¥ `exclusiveOr(_:)` –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –Ω–æ–≤–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –∏–∑ –∑–Ω–∞—á–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–µ –Ω–µ –ø–æ–≤—Ç–æ—Ä—è—é—Ç—Å—è –≤–æ –≤—Ö–æ–¥–Ω—ã—Ö –º–Ω–æ–∂–µ—Å—Ç–≤–∞—Ö.
 
 
 * –º–µ—Ç–æ–¥ `union(_:)` –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –Ω–æ–≤–æ–≥–æ –º–Ω–æ–∂–µ—Å—Ç–≤–∞, —Å–æ—Å—Ç–æ—è—â–µ–≥–æ –∏–∑ –≤—Å–µ—Ö –∑–Ω–∞—á–µ–Ω–∏–π –æ–±–æ–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤.
 
 
 * –º–µ—Ç–æ–¥ `subtract(_:)` –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å–æ –∑–Ω–∞—á–µ–Ω–∏—è–º–∏, –∏—Å–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–∞—â–∏—Ö —Ç–æ–ª—å–∫–æ –æ–¥–Ω–æ–º—É –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤.
